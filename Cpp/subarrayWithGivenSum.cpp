class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(int arr[], int n, long long sum)
    {
        if(sum==0) return {-1};
        
        int s=0, e=0;
        long long csum = 0; //current sum
        while(e <= n) {
            while(csum < sum) csum += arr[e++];
            if(sum == csum) return {s+1,e};
            if(csum > sum) csum-=arr[s++];       
        }
        return {-1};
    }
};

